    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        // Equals Button 
        
        //Set first and second number from string to integers so they can be computed
        Inum1 = Integer.parseInt(num1);
        Inum2 = Integer.parseInt(num2);

        // Check which operation button was clicked
        // Calculate the corresponding computation
        if (oppA == true) {
            Ianswer = Inum1 + Inum2;
        } else if (oppS == true) {
            Ianswer = Inum1 - Inum2;
        } else if (oppM == true) {
            Ianswer = Inum1 * Inum2;
        } else if (oppD == true) {
            Ianswer = Inum1 / Inum2;
        }

        // Change the answer from an integer to a string and set label to be the string
        Sanswer = Integer.toString(Ianswer);
        jLabel1.setText(Sanswer);

        // Set first number equal to the sum, and set operation and second number variables back to initial values
        num1 = Sanswer;
        num2 = "";
        opp = false;
        oppA = false;
        oppS = false;
        oppM = false;
        oppD = false;
        
    }//GEN-LAST:event_jButton16ActionPerformed

